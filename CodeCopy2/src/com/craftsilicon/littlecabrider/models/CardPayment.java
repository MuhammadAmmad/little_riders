package com.craftsilicon.littlecabrider.models;

public class CardPayment {
	private String rowID;
	private String cardUserID;
	private String userCardName;
	private String userCardNickName;
	private String userCardNumber;
	private String userCardExpiryDate;
	
	public String getRowID() {
		return rowID;
	}
	public void setRowID(String rowID) {
		this.rowID = rowID;
	}
	public String getCardUserID() {
		return cardUserID;
	}
	public void setCardUserID(String cardUserID) {
		this.cardUserID = cardUserID;
	}
	public String getUserCardName() {
		return userCardName;
	}
	public void setUserCardName(String userCardName) {
		this.userCardName = userCardName;
	}
	public String getUserCardNickName() {
		return userCardNickName;
	}
	public void setUserCardNickName(String userCardNickName) {
		this.userCardNickName = userCardNickName;
	}
	public String getUserCardNumber() {
		return userCardNumber;
	}
	public void setUserCardNumber(String userCardNumber) {
		this.userCardNumber = userCardNumber;
	}
	public String getUserCardExpiryDate() {
		return userCardExpiryDate;
	}
	public void setUserCardExpiryDate(String userCardExpiryDate) {
		this.userCardExpiryDate = userCardExpiryDate;
	}
	
	

}
